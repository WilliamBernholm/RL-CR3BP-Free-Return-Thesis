def summarize_by_cell(data: Dict[str, np.ndarray]) -> List[Dict[str, Any]]:
    sigma_pos = np.asarray(data["sigma_pos_m"], dtype=float)
    sigma_vel = np.asarray(data["sigma_vel_mps"], dtype=float)
    ppo = np.asarray(data["ppo_success"], dtype=bool)
    nominal = np.asarray(data["nominal_success"], dtype=bool)

    unique_cells = sorted(set(zip(sigma_pos.tolist(), sigma_vel.tolist())), key=lambda p: case_sort_key({"sigma_pos_m": p[0], "sigma_vel_mps": p[1]}))

    rows: List[Dict[str, Any]] = []
    for sp, sv in unique_cells:
        mask = np.isclose(sigma_pos, sp) & np.isclose(sigma_vel, sv)
        n = int(np.sum(mask))
        if n == 0:
            continue
        ppo_rate = float(np.mean(ppo[mask]))
        nominal_rate = float(np.mean(nominal[mask]))
        rows.append({
            "case": case_label(sp, sv),
            "sigma_pos_m": float(sp),
            "sigma_vel_mps": float(sv),
            "n": n,
            "ppo_success_rate": ppo_rate,
            "nominal_success_rate": nominal_rate,
            "delta_rate": ppo_rate - nominal_rate,
        })

    rows.append({
        "case": "Total",
        "sigma_pos_m": np.nan,
        "sigma_vel_mps": np.nan,
        "n": int(len(ppo)),
        "ppo_success_rate": float(np.mean(ppo)),
        "nominal_success_rate": float(np.mean(nominal)),
        "delta_rate": float(np.mean(ppo) - np.mean(nominal)),
    })

    return rows


def save_single_mode_table(rows, mode_name, output_dir):

    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    tex_path = output_dir / f"sensitivity_{mode_name}_ppo_vs_nominal_table.tex"

    with open(tex_path, "w", encoding="utf-8") as f:

        f.write("\\begin{table}[t]\n")
        f.write("\\centering\n")
        f.write("\\small\n")
        f.write("\\begin{tabular}{lrrrrrr}\n")
        f.write("\\hline\n")
        f.write("Case & $\\sigma_r$ & $\\sigma_v$ & $N$ & PPO & Nom. & $\\Delta$ \\\\\n")
        f.write(" & [m] & [m/s] & & [\\%] & [\\%] & [pp] \\\\\n")
        f.write("\\hline\n")

        for r in rows:

            if r["case"] == "Total":
                sig_r = "--"
                sig_v = "--"
                f.write("\\hline\n")
            else:
                sig_r = f"{r['sigma_pos_m']:.0f}"
                sig_v = f"{r['sigma_vel_mps']:.0f}"

            f.write(
                f"{r['case']} & "
                f"{sig_r} & "
                f"{sig_v} & "
                f"{r['n']} & "
                f"{100*r['ppo_success_rate']:.1f} & "
                f"{100*r['nominal_success_rate']:.1f} & "
                f"{100*r['delta_rate']:+.1f} \\\\\n"
            )

        f.write("\\hline\n")
        f.write("\\end{tabular}\n")
        f.write(
            f"\\caption{{{mode_name.upper()} sensitivity validation comparing PPO adaptation against nominal-action replay.}}\n"
        )
        f.write(
            f"\\label{{tab:{mode_name}_sensitivity}}\n"
        )
        f.write("\\end{table}\n")

    return tex_path


