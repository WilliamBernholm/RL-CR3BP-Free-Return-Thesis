def plot_ppo_success_scatter(data: Dict[str, np.ndarray], output_dir: Path) -> Dict[str, Any]:
    apply_thesis_style()

    mode = str(data["mode"][0]).lower()
    mode_label = MODE_INFO[mode]["label"]
    x = np.asarray(data["x_km"], dtype=float)
    y = np.asarray(data["y_mps"], dtype=float)
    success = np.asarray(data["ppo_success"], dtype=bool)

    fig, ax = plt.subplots(figsize=get_figsize(FIGSIZE_KIND))

    fail = ~success

    ax.scatter(
        x[fail],
        y[fail],
        s=POINT_SIZE,
        color=FAILURE_COLOR,
        alpha=POINT_ALPHA,
        linewidths=0,
        zorder=5,
    )

    ax.scatter(
        x[success],
        y[success],
        s=POINT_SIZE,
        color=SUCCESS_COLOR,
        alpha=POINT_ALPHA,
        edgecolors="black",
        linewidths=0.15,
        zorder=10,
    )

    clean_axis(ax, grid=SHOW_GRID)
    ax.set_xlabel(r"Actual position perturbation norm [km]")
    ax.set_ylabel(r"Actual velocity perturbation norm [m/s]")

    if SHOW_TITLE:
        ax.set_title(ieee_title(f"PPO-{mode_label} sensitivity scatter"))

    set_limits(ax, x, y)
    add_success_legend(ax)

    success_rate = 100.0 * float(np.mean(success))
    ax.text(
        0.02,
        0.98,
        f"PPO success: {success_rate:.1f}%",
        transform=ax.transAxes,
        ha="left",
        va="top",
        fontsize=9,
        bbox=dict(facecolor="white", edgecolor="black", alpha=0.85),
    )

    out_name = f"sensitivity_{mode}_success_scatter"
    save_thesis_figure(fig, Path(output_dir) / out_name)
    plt.close(fig)

    print(f"[OK] Saved {out_name}.png/.pdf from {Path(str(data['path'][0])).name}")

    return {
        "mode": mode,
        "n": int(len(success)),
        "ppo_success_rate": float(np.mean(success)),
        "nominal_success_rate": float(np.mean(data["nominal_success"])),
    }


