"""
Final thesis sensitivity scatter plots and PPO-vs-nominal validation table.

New validation-data format
--------------------------
This script reads the validation CSV files generated by the PPO-vs-nominal
replay validation script:

    Final Plots/data/sensitivity_tli/tli_validation_samples.csv
    Final Plots/data/sensitivity_mcc/mcc_validation_samples.csv

It outputs:

    sensitivity_tli_success_scatter.png/.pdf
    sensitivity_mcc_success_scatter.png/.pdf
    sensitivity_ppo_vs_nominal_table.csv
    sensitivity_ppo_vs_nominal_table.tex
    sensitivity_ppo_vs_nominal_table.txt

The public main(data_dir, output_dir) signature is intentionally kept unchanged
so this file still works when called from plot_all.py as:

    from scripts.plot_sensitivity_analysis import main as run
    run(DATA_DIR, THESIS_DIR)

Scatter interpretation
----------------------
The scatter plots show the PPO rerun result for each perturbed initial condition.
The x-axis is the actual position perturbation norm in km and the y-axis is the
actual velocity perturbation norm in m/s.

Table interpretation
--------------------
The LaTeX table compares PPO adaptation against replaying the nominal action
sequence for each perturbation cell. Positive Delta means PPO has a higher
success rate than nominal replay.
"""

