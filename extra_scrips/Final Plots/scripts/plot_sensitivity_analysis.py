# BASIC HELPERS
# ============================================================

def parse_bool(x: Any) -> bool:
    if isinstance(x, bool):
        return x
    if isinstance(x, (np.bool_,)):
        return bool(x)
    s = str(x).strip().lower()
    return s in ("true", "1", "yes", "y", "success")


def parse_float(row: Dict[str, str], key: str) -> float:
    try:
        return float(row[key])
    except Exception as exc:
        raise ValueError(f"Could not parse column '{key}' as float. Bad row: {row}") from exc


def format_float_for_table(x: float) -> str:
    if x is None or not np.isfinite(float(x)):
        return "--"
    x = float(x)
    if abs(x - round(x)) < 1e-9:
        return f"{int(round(x))}"
    return f"{x:.3g}"


def format_rate(x: float) -> str:
    if x is None or not np.isfinite(float(x)):
        return "--"
    return f"{100.0 * float(x):.1f}"


def format_delta(delta_fraction: float) -> str:
    if delta_fraction is None or not np.isfinite(float(delta_fraction)):
        return "--"
    value = 100.0 * float(delta_fraction)
    return f"{value:+.1f}"


def case_label(sigma_pos_m: float, sigma_vel_mps: float) -> str:
    pos_zero = np.isclose(float(sigma_pos_m), 0.0)
    vel_zero = np.isclose(float(sigma_vel_mps), 0.0)

    if pos_zero and vel_zero:
        return "Nominal"
    if (not pos_zero) and vel_zero:
        return "Position only"
    if pos_zero and (not vel_zero):
        return "Velocity only"
    return "Position+velocity"


def case_sort_key(row: Dict[str, Any]) -> Tuple[int, float, float]:
    sp = float(row["sigma_pos_m"])
    sv = float(row["sigma_vel_mps"])
    label = case_label(sp, sv)
    order = {
        "Nominal": 0,
        "Position only": 1,
        "Velocity only": 2,
        "Position+velocity": 3,
    }.get(label, 10)
    return order, sp, sv


def set_limits(ax, x, y):
    x = np.asarray(x, dtype=float)
    y = np.asarray(y, dtype=float)
    x = x[np.isfinite(x)]
    y = y[np.isfinite(y)]

    if len(x) == 0 or len(y) == 0:
        return

    xmax = np.nanmax(x)
    ymax = np.nanmax(y)
    xmin = np.nanmin(x)
    ymin = np.nanmin(y)

    xpad = max(0.05 * max(abs(xmax), 1.0), 0.25)
    ypad = max(0.08 * max(abs(ymax), 1.0), 1.0)

    ax.set_xlim(min(-xpad, xmin - 0.2 * xpad), xmax + xpad)
    ax.set_ylim(min(-ypad, ymin - 0.2 * ypad), ymax + ypad)


def add_success_legend(ax):
    fail_proxy = ax.scatter([], [], s=POINT_SIZE, color=FAILURE_COLOR, label="Failure")
    success_proxy = ax.scatter([], [], s=POINT_SIZE, color=SUCCESS_COLOR, label="Success")
    ax.legend(handles=[fail_proxy, success_proxy], loc="best", frameon=True)


