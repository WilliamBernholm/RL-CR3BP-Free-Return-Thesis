def find_validation_csv(data_dir: Path, mode: str) -> Optional[Path]:
    """Find the expected validation CSV for one mode."""
    data_dir = Path(data_dir)
    info = MODE_INFO[mode]

    preferred = data_dir / info["folder"] / info["csv"]
    if preferred.exists():
        return preferred

    # Helpful fallback if the user accidentally keeps the file one level deeper.
    candidates = sorted((data_dir / info["folder"]).rglob(info["csv"])) if (data_dir / info["folder"]).exists() else []
    if candidates:
        return candidates[0]

    # Last-resort fallback under the full data folder.
    candidates = sorted(data_dir.rglob(info["csv"]))
    if candidates:
        return candidates[0]

    return None


def read_validation_csv(path: Path, mode: str) -> Dict[str, np.ndarray]:
    rows: List[Dict[str, str]] = []
    with open(path, "r", encoding="utf-8", newline="") as f:
        reader = csv.DictReader(f)
        fieldnames = reader.fieldnames or []
        missing = [c for c in REQUIRED_COLUMNS if c not in fieldnames]
        if missing:
            raise ValueError(
                f"{path} is missing required columns: {missing}\n"
                f"Available columns are: {fieldnames}"
            )
        rows = list(reader)

    if not rows:
        raise ValueError(f"{path} contains no data rows.")

    sigma_pos = np.array([parse_float(r, "sigma_pos_m") for r in rows], dtype=float)
    sigma_vel = np.array([parse_float(r, "sigma_vel_mps") for r in rows], dtype=float)
    dx = np.array([parse_float(r, "dx_m") for r in rows], dtype=float)
    dy = np.array([parse_float(r, "dy_m") for r in rows], dtype=float)
    dvx = np.array([parse_float(r, "dvx_mps") for r in rows], dtype=float)
    dvy = np.array([parse_float(r, "dvy_mps") for r in rows], dtype=float)
    ppo_success = np.array([parse_bool(r[PPO_SUCCESS_COL]) for r in rows], dtype=bool)
    nominal_success = np.array([parse_bool(r[NOMINAL_SUCCESS_COL]) for r in rows], dtype=bool)

    x_km = np.sqrt(dx ** 2 + dy ** 2) / 1000.0
    y_mps = np.sqrt(dvx ** 2 + dvy ** 2)

    return {
        "mode": np.array([mode]),
        "path": np.array([str(path)]),
        "sigma_pos_m": sigma_pos,
        "sigma_vel_mps": sigma_vel,
        "dx_m": dx,
        "dy_m": dy,
        "dvx_mps": dvx,
        "dvy_mps": dvy,
        "x_km": x_km,
        "y_mps": y_mps,
        "ppo_success": ppo_success,
        "nominal_success": nominal_success,
    }


