# MAIN ENTRY POINT USED BY plot_all.py
# ============================================================

def main(data_dir=None, output_dir=None):
    """
    Main entry point.

    Kept compatible with plot_all.py:
        run(DATA_DIR, THESIS_DIR)
    """
    if data_dir is None:
        data_dir = FINAL_ROOT / "data"

    if output_dir is None:
        output_dir = FINAL_ROOT / "outputs" / "thesis_ready"

    data_dir = Path(data_dir)
    output_dir = Path(output_dir)
    output_dir.mkdir(parents=True, exist_ok=True)

    print("\n" + "-" * 72)
    print("Sensitivity validation plots and table")
    print("-" * 72)

    loaded: Dict[str, Dict[str, np.ndarray]] = {}
    mode_summaries: Dict[str, List[Dict[str, Any]]] = {}

    for mode in ["tli", "mcc"]:
        csv_path = find_validation_csv(data_dir, mode)
        if csv_path is None:
            expected = data_dir / MODE_INFO[mode]["folder"] / MODE_INFO[mode]["csv"]
            print(f"[WARN] Missing {mode.upper()} validation CSV. Expected:\n       {expected}")
            continue

        data = read_validation_csv(csv_path, mode)
        loaded[mode] = data
        plot_ppo_success_scatter(data, output_dir)
        mode_summaries[mode] = summarize_by_cell(data)

        print(
            f"[OK] {mode.upper()}: N={len(data['ppo_success'])}, "
            f"PPO SR={100*np.mean(data['ppo_success']):.1f}%, "
            f"Nominal SR={100*np.mean(data['nominal_success']):.1f}%"
        )

    if not loaded:
        print("[WARN] No sensitivity validation CSV files found. No sensitivity plots/tables were generated.")
        return []

    for mode in ["tli", "mcc"]:

        if mode not in mode_summaries:
            continue

        tex_path = save_single_mode_table(
            mode_summaries[mode],
            mode,
            output_dir,
        )

        print(f"[OK] Saved {tex_path.name}")

    return mode_summaries


if __name__ == "__main__":
    main()
