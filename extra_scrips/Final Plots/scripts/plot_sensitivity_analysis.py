FIGSIZE_KIND = "single"

POINT_SIZE = 14
POINT_ALPHA = 0.85

SUCCESS_COLOR = "gold"
FAILURE_COLOR = "#1f4e79"

SHOW_TITLE = True
SHOW_GRID = True

# Success definitions used for the new validation CSV format.
# The clean flag excludes trajectories that later impact Earth/Moon.
PPO_SUCCESS_COL = "rerun_ppo_clean_success_no_impact"
NOMINAL_SUCCESS_COL = "nominal_clean_success_no_impact"

# Required validation CSV columns.
REQUIRED_COLUMNS = [
    "sigma_pos_m",
    "sigma_vel_mps",
    "dx_m",
    "dy_m",
    "dvx_mps",
    "dvy_mps",
    PPO_SUCCESS_COL,
    NOMINAL_SUCCESS_COL,
]

# Expected validation folders under Final Plots/data.
MODE_INFO = {
    "tli": {
        "folder": "sensitivity_tli",
        "csv": "tli_validation_samples.csv",
        "label": "TLI",
    },
    "mcc": {
        "folder": "sensitivity_mcc",
        "csv": "mcc_validation_samples.csv",
        "label": "MCC",
    },
}


